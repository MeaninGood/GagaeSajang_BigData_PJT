package object;

public class CodeAndGU {

    String code;
    String guName;

    public CodeAndGU(String code, String guName) {
        this.code = code;
        this.guName = guName;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getGuName() {
        return guName;
    }

    public void setGuName(String guName) {
        this.guName = guName;
    }
}
